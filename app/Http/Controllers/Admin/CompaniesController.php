    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {


        $add=new companie();
        $add->name=$request->name;
        $add->description=$request->description;
        $add->status=1;
        $add->save();
        return redirect()->back();

    }

